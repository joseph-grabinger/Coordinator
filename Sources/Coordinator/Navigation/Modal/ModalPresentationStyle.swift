/// An enumeration representing the supported styles for presenting modals.
public enum ModalPresentationStyle {

    /// Presents a view as a sheet over the current context.
    case sheet

    /// Presents a view as a full-screen cover, obscuring the entire screen.
    case fullScreenCover
}
